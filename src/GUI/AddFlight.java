    private void loadcities(){
        from1.removeAllItems();
        from1.addItem("--- Select ---");
        to1.removeAllItems();
        to1.addItem("--- Select ---");
        airline.removeAllItems();
        airline.addItem("--- Select ---");
        try {
            ResultSet rs = DataBaseConnection.getData("SELECT airport_name FROM AIRPORT ");
            while(rs.next()){
                from1.addItem(rs.getString(1));
                to1.addItem(rs.getString(1));
            }
            rs = DataBaseConnection.getData("SELECT airline_name FROM AIRLINE");
            while(rs.next()){
                airline.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Airline.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void LoadPlains(){
        regno.removeAllItems();
        regno.addItem("--- Select ---");
        String vairline = ""+ airline.getSelectedItem();
        try {
            ResultSet rs = DataBaseConnection.getData("SELECT reg_no FROM AIRPLANE natural join airline where airline_name = '"+vairline+"' ");
            while(rs.next()){
                regno.addItem(rs.getString(1));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Airline.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void loadcrew(){
        p1.removeAllItems();
        p1.addItem("--- Select ---");
        p2.removeAllItems();
        p2.addItem("--- Select ---");
        m1.removeAllItems();
        m1.addItem("--- Select ---");
        m2.removeAllItems();
        m2.addItem("--- Select ---");
        String vairline = ""+ airline.getSelectedItem();
        try {
            ResultSet rs = DataBaseConnection.getData("SELECT crew_id FROM CREW natural join airline WHERE role ='Pilot' and airline_name= '"+vairline+"' ");
            while(rs.next()){
                p1.addItem(rs.getString(1));
                p2.addItem(rs.getString(1));
            }
            rs = DataBaseConnection.getData("SELECT crew_id FROM CREW natural join airline WHERE role ='Air Hostess' and airline_name= '"+vairline+"' ");
            while(rs.next()){
                m1.addItem(rs.getString(1));
                m2.addItem(rs.getString(1));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Airline.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
