    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
                String seq = "";
        if(p1.getSelectedItem().toString().equals(p2.getSelectedItem().toString())){
            p2.setSelectedIndex(0);
            p2.requestFocus();
            getToolkit().beep();
        } else if(m1.getSelectedItem().toString().equals(m2.getSelectedItem().toString())){
            m2.setSelectedIndex(0);
            m2.requestFocus();
            getToolkit().beep();
        } else if(validatefelds()){
            try {
                ResultSet rs = DataBaseConnection.getData("select seq_flight.nextval from dual");
                if(rs.next()){
                    seq = rs.getString(1);
                }
                DataBaseConnection.insertData("INSERT INTO FLIGHT_DETAILS "
                        + "(FLIGHT_ID, REG_NO, DEPARTURE_LOC, ARRIVAL_LOC, DEPARTURE_DATE, ARRIVAL_DATE, FLIGHT_TIME) VALUES ("+seq+", '"
                        + regno.getSelectedItem().toString()+"', '"
                        +to1.getSelectedItem().toString()+ "', '"
                        + from1.getSelectedItem().toString()+"', TO_DATE('"+getfromdate()+ "', 'YYYY-MM-DD'), TO_DATE('"+ gettodate()+"', 'YYYY-MM-DD'), '"
                        + time.getSelectedItem().toString()+"')");
                
                try {
                    
                DataBaseConnection.insertData("INSERT INTO FLIGHT_CREW (FLIGHT_ID, CREW_ID) VALUES ("+seq+", '"+p1.getSelectedItem().toString()+"')");
                DataBaseConnection.insertData("INSERT INTO FLIGHT_CREW (FLIGHT_ID, CREW_ID) VALUES ("+seq+", '"+p2.getSelectedItem().toString()+"')");
                DataBaseConnection.insertData("INSERT INTO FLIGHT_CREW (FLIGHT_ID, CREW_ID) VALUES ("+seq+", '"+m1.getSelectedItem().toString()+"')");
                DataBaseConnection.insertData("INSERT INTO FLIGHT_CREW (FLIGHT_ID, CREW_ID) VALUES ("+seq+", '"+m2.getSelectedItem().toString()+"')");
                
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
//                DataBaseConnection.deleteData("delete from FLIGHT_DETAILS where flight_id='"+seq+"'");
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
//                    JOptionPane.showMessageDialog(this, ex);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    
    
    
